    if question_text:
        query = f'在{src_short}第{page}页中，{question_text}？'
    else:
        query = f'在{src_short}第{page}页中，{company}的{indicator}是多少？'
    
    samples.append({
        "query": query,
        "query_type": "fact",
        "ground_truth_answer": value,
        "ground_truth_chunk_id": chunk_id,
        "source_file": source_file,
        "page_num": page,
        "company": company,
        "indicator": indicator
    })

