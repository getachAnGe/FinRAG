errors = []
for s in samples:
    cid_str = s['ground_truth_chunk_id']
    for cid in cid_str.split(','):
        c = chunks.get(cid.strip())
        if not c:
            errors.append(f'chunk不存在: {cid}')
            continue
        # Verify source and page for factual/summary
        if s['source_file']:
            src_ok = s['source_file'] in c['source']
            page_ok = str(c['page_num']) == str(s['page_num'])
            if not (src_ok and page_ok):
                errors.append(f'{cid}: 期望{s["source_file"]} p{s["page_num"]}, 实际{c["source"]} p{c["page_num"]}')

if errors:
    print(f'存在 {len(errors)} 个错误:')
    for e in errors:
        print(f'  {e}')
else:
    print('全部验证通过!')

