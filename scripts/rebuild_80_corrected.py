with open('data/chunks/all_chunks.json', 'r', encoding='utf-8') as f:
    chunks = json.load(f)

def find_cid(source, page, value):
    val_clean = value.replace(',', '')
    for c in chunks:
        if source not in c['source']: continue
        if str(c['page_num']) != str(page): continue
        text = c['text']
        if val_clean in text.replace(',', ''):
            return c['id']
    # fallback: company-only match
    parts = source.replace('.pdf','').split('_')
    if len(parts) >= 2:
        company = parts[1]
        for c in chunks:
            if company not in c['source']: continue
            if str(c['page_num']) != str(page): continue
            text = c['text']
            if val_clean in text.replace(',', ''):
                return c['id']
    return None

def verify_chunk(cid, expect_source, expect_page):
    c = next((x for x in chunks if x['id'] == cid), None)
    if not c: return False, 'not found'
    source_ok = expect_source in c['source']
    page_ok = str(c['page_num']) == str(expect_page)
    return source_ok and page_ok, f'{c["source"]} p{c["page_num"]}'

samples = []
errors = []

def add(q, ans, src, page, comp, ind, qtype, cid_hardcoded=None):
    if cid_hardcoded:
        c = next((x for x in chunks if x['id'] == cid_hardcoded), None)
        if not c:
            errors.append(f'chunk_id不存在: {cid_hardcoded}')
            return
        cid = cid_hardcoded
    else:
        cid = find_cid(src, page, ans)
    if not cid:
        errors.append(f'找不到chunk: [{comp}] {src} 第{page}页 {ind}={ans}')
        return
    ok, info = verify_chunk(cid, src, page)
    if not ok:
        errors.append(f'chunk验证失败: [{comp}] 期望{src} p{page}, 实际{info}')
        return
    samples.append({
        "query": q, "query_type": qtype,
        "ground_truth_answer": ans,
        "ground_truth_chunk_id": cid,
        "source_file": src, "page_num": page,
        "company": comp, "indicator": ind
    })

def add_multi(q, ans, pairs, comp, ind, qtype):
    """pairs = [(src, page, search_val), (src, page, search_val)]"""
    cids = []
    for src, page, search_val in pairs:
        cid = find_cid(src, page, search_val)
        if not cid:
            errors.append(f'❌ 找不到chunk: {src} p{page} val={search_val}')
            return
        cids.append(cid)
    samples.append({
        "query": q, "query_type": qtype,
        "ground_truth_answer": ans,
        "ground_truth_chunk_id": ','.join(cids),
        "source_file": '', "page_num": '',
        "company": comp, "indicator": ind
    })

